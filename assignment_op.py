# assignment operators
a = 10
# addition assignment
a+=5
print("a+=5:",a)
# subtraction assignment
a-=5
print("a-=5:",a)
# multiplication assignment
a*=5
print("a*=5:",a)
# division assignment
a/=5
print("a/=5:",a)
# floor division assignment
a//=5
print("a//=5:",a)
# remainder assignment
a%=5
print("a%=5:",a)
# exponent assignment
a**=5
print("a**=5:",a)